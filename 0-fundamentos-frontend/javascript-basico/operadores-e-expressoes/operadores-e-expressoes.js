/*
OPERADORES LOGICOS
! negação
&& E lógico
|| OU lógico
*/
console.log(!true)
console.log(true && true)
console.log(false || true)

/*OPERADORES ARITMETICOS*/
console.log(1+1) //adicao
console.log(1-1) //subtracao
console.log(+1) //converte para números (se já nao for)
console.log(-1) //Inversao de sinal
console.log(1/1) //divisao
console.log(1*1) //multiplicação
console.log(1%1) //resto da divisão de x por y
console.log(1**1) //exponenciação

//OPERADORES DE INCREMENTO/DECREMENTO
var x = 1
console.log(x++) // retorna x e depois incrementa
console.log(x++) //retorna x e depois decrementa
console.log(++x) //Incrementa e depois retorna x
console.log(--x) //Decrementa e depois retorna x

//OPERADORES DE COMPARAÇÃO
console.log(3>2) //maior
console.log(1<2) //menor
console.log(3>=2) //maior ou igual
console.log(1<=2) //menor ou igual

//OPERADOR DE IGUALDADE
console.log(3==3) //igual
console.log(3===3) //exatamanete igual
console.log(3!=3) //diferente
console.log(3!=3)//não exatamente igual
